# None
print(type(None))

# Bool
print(type(True))
print(type(False))

# 숫자형
print(type(5))
print(type(3.14))
print(type(2 + 3j))

# 문자형
print(type("문자열"))

# 시퀀스 자료형
print(type((1,2,4)))
print(type([1,2,4]))

# dict
print(type({"A":10, "B":20}))

# set
print(type({"1","2","3"}))

# function
def f():
    return 1
print(type(f))